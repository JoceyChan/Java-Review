public class Main {
   public static void main(String[] args) {
       for(int i = 0; i < 5; i++){
    //initalizin i to 0; 
    //check if i is less than 5, moves to the next line and gets execute; 
    //then i gets incremented by 1 and repeat the processes till i is greater or = to 5 and stops before printing
        System.out.println("Hello World");
       }

       for(int j = 5; j > 0; j--){
        //initalizin j to 0; 
        //check if j is greater than 0, moves to the next line and gets execute; 
        //then j gets decremented by 1 and repeat the processes till j is less than 0 and stops 
            System.out.println("Hello World " + j);
           }
   } 
}
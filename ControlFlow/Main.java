public class Main {
    public static void main(String[] args){
        int x = 1;
        int y = 1;

        System.out.println(x == y); // equals
        System.out.println(x != y); // "!=" does not equal
        System.out.println(x < y); // x is less than y
        System.out.println(x <= y); // x is less than or equal to y


    }
}